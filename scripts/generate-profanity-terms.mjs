#!/usr/bin/env node
// Regenerates the profanity blocklist used by:
//   - src/lib/profanityTerms.generated.ts (Vitest + JS helper)
//   - supabase/migrations/20260516000026_content_moderation.sql (DB trigger)
//
// Source of truth: the `bad-words` npm package's wordlist
// (https://www.npmjs.com/package/bad-words). Re-run after bumping the
// dependency to pick up new entries.

import { writeFile } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import { Filter } from 'bad-words';

const HERE = dirname(fileURLToPath(import.meta.url));
const TS_OUT = resolve(HERE, '../src/lib/profanityTerms.generated.ts');
const SQL_FRAGMENT_OUT = resolve(HERE, '../supabase/queries/profanity_terms.generated.sql');

function normalizeTerm(raw) {
  return String(raw ?? '')
    .toLowerCase()
    .trim()
    // Keep ASCII letters/digits/spaces only. Anything with regex
    // metacharacters or non-ASCII letters gets dropped — the cost is
    // a smaller list, the gain is a regex that's safe to embed verbatim.
    .replace(/[^a-z0-9 ]/g, '')
    .replace(/\s+/g, ' ');
}

function buildTerms() {
  const filter = new Filter();
  const set = new Set();
  for (const raw of filter.list) {
    const normalized = normalizeTerm(raw);
    if (normalized.length >= 3) {
      set.add(normalized);
    }
  }
  return Array.from(set).sort();
}

function escapeForJsString(term) {
  return term.replace(/\\/g, '\\\\').replace(/'/g, "\\'");
}

function buildRegexAlternation(terms) {
  // No need to regex-escape — terms are already filtered to [a-z0-9 ] only.
  // The space character is the only multi-char concern; postgres treats it
  // literally inside the alternation.
  return terms.join('|');
}

const terms = buildTerms();
const alternation = buildRegexAlternation(terms);

const tsBody = `// AUTO-GENERATED by scripts/generate-profanity-terms.mjs. Do not edit by hand.
// Source: the \`bad-words\` npm package, normalized to [a-z0-9 ]+ and deduped.
// Re-run \`node scripts/generate-profanity-terms.mjs\` to refresh.

export const OBJECTIONABLE_TERMS: readonly string[] = Object.freeze([
${terms.map((term) => `  '${escapeForJsString(term)}',`).join('\n')}
]);

export const OBJECTIONABLE_TERMS_COUNT = ${terms.length};
`;

const sqlBody = `-- AUTO-GENERATED by scripts/generate-profanity-terms.mjs. Do not edit by hand.
-- Source: the \`bad-words\` npm package, normalized to [a-z0-9 ]+ and deduped.
--
-- To refresh the production blocklist after re-running this script, add a new
-- migration containing:
--
--   create or replace function public.message_content_appears_objectionable(input text)
--   returns boolean
--   language sql
--   immutable
--   as $$
--     select lower(coalesce(input, '')) ~* '\\m(<paste the alternation from below>)\\M';
--   $$;
--
-- Embedded regex alternation matching ${terms.length} blocklisted terms:
${alternation}
`;

await writeFile(TS_OUT, tsBody, 'utf8');
await writeFile(SQL_FRAGMENT_OUT, sqlBody, 'utf8');

console.log(`Generated ${terms.length} terms.`);
console.log(`  → ${TS_OUT}`);
console.log(`  → ${SQL_FRAGMENT_OUT}`);
